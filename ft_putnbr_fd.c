#include "libft.h"
#include <limits.h>

void	ft_putnbr_fd(int nb, int fd)
{
	if (nb > 9)
	{
		ft_putnbr_fd(nb / 10, fd);
		ft_putchar_fd(nb % 10 + '0', fd);
	}
	else if (nb == INT_MIN)
	{
		ft_putnbr_fd(nb / 10, fd);
		ft_putnbr_fd(-(nb % 10), fd);
	}
	else if (nb < 0)
	{
		ft_putchar_fd('-', fd);
		ft_putnbr_fd(-nb, fd);
	}
	else
		ft_putchar_fd(nb % 10 + '0', fd);
}
